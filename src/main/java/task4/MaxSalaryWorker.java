package task4;


public class MaxSalaryWorker {

    private final String name;
    private final long salary;

    public MaxSalaryWorker(String name, long salary) {
        this.name = name;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public long getSalary() {
        return salary;
    }
}
